<?php if ('Loggable' === $extension_type): ?>
<p><strong>Gedmo Loggable</strong> automatically creates audit log entries for tracked fields:</p>
<ul>
<li>Each field change triggers an INSERT into the changelog table</li>
<li>With 10 fields and 1000 updates per day, that's 10,000 additional INSERTs</li>
<li>Log tables grow unbounded — cleanup policies must be implemented</li>
<li>Query performance degrades as log tables grow</li>
</ul>


<li><strong>Key-value translation store</strong> — Separate service for translation management</li>
<li><strong>Database-level i18n</strong> — Use native PostgreSQL jsonb or MySQL JSON type</li>
<li><strong>Third-party services</strong> — Offload translation management to specialized services</li>
</ul>

<h4>If you keep Translatable</h4>
<ul>
<li>Use eager loading for translations in frequently accessed queries</li>
<li>Implement query result caching at the application level</li>
